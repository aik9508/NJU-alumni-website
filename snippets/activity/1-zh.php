<h3>南京大学校友会携手长江商学院欧洲办公室及海外留学人员创业者协会举办的以创业与创新为主题的活动</h3>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;南京大学校友会携手长江商学院欧洲办公室及海外留学人员创业者协会举办的以创业与创新为主题的活动于2月13日下午在中国驻法国大使馆教育处圆满举行。中国驻法大使馆教育处公使马燕生公参、南京大学法国校友会会长季红、长江商学院欧洲首席代表季波博士、法国海外留学人员创业者协会会长李天伦等人士出席此次活动。</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;马燕生公参对此次活动给予了大力鼓励和支持。他在致辞中说，李克强总理早在2014年就提出“大众创新、万众创业”的口号，留法人员虽然虽然身在法国，但国家依然给留法人员创新创业给予充分支持。长江商学院欧洲首席代表季波博士介绍了长江商学院总裁班项目并与大家分享了对“颠覆性的创新者具有怎样的特征”这样一个话题的思考。季博士认为,创造能力是完全可以培养的。曾有研究所通过对5000多位创新人才的特点进行分析，总结出创新者的五个共同特质：提问，观察，社交，实验，关联思考。</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;季博士对五大创新特质进行了详细阐释，对提问，观察，社交，实验，关联思考。这五个创新者的DNA进行解剖，并与现场观众进行互动，将现场气氛带到小高潮。随后，法国海外留学人员创业者协会会长李天伦介绍了法国海外留学人员协会在法国和中国提供怎样的服务。李会长说，法国海外留学人员创业者协会主要为想创业的留法学生、法国华侨和想在中国创业的法国人提供办公空间、导师指导等服务。</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;李会长认为，一个负责任的孵化器平台的目的并不应该停留在简单的将项目引进过来。而是应该在前期严格挑选好的项目, 然后陪伴项目一起成长，力助项目成功。他热情鼓励大家踊跃参加今年“赢在苏州”的创新大赛。</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;在随后的提问环节，大家积极提问，一起头脑风暴。季博士和李会长还给现场驻法华人企业家代表提出在欧洲发展的宝贵意见。</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;最后，大家享用了法国蓝带协会准备的Cocktail，活动在轻松愉快的气氛中圆满结束。</p>

